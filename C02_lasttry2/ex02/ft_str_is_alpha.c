int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if ((*(str + i) > 'z' || *(str + i) < 'A'))
			return (0);
		if ((*(str + i) < 97 && *(str + i) > 90))
			return (0);
		i++;
	}
	return (1);
}
